class LabelStructureThing {
  /**
   * @param {string} id
   * @param {string} name
   * @param {string} shape
   */
  constructor(id, name, shape) {
    /**
     * @type {string}
     */
    this.id = id;

    /**
     * @type {string}
     */
    this.name = name;

    /**
     * @type {string}
     */
    this.shape = shape;
  }
}

export default LabelStructureThing;
